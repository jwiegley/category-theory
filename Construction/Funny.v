(* The category.  Objects pair up ONLY here, accessed via fst/snd, never
   pattern matches (Construction/Product.v style).  Note the direction
   convention shared with Construction/Free.v: words read source-to-target,
   so [t ∘ u] is "u then t", i.e. [fapp u t]. *)
Definition Funny (C D : Category) : Category := {|
  obj     := C * D;
  hom     := fun x y => FunHom (fst x) (snd x) (fst y) (snd y);
  homset  := fun _ _ => FunHom_Setoid;
  id      := fun _ => fnil;
  compose := fun _ _ _ t u => fapp u t;
  compose_respects := fun _ _ _ => funny_compose_respects;
  id_left  := fun _ _ f => feq_fapp_fnil f;
  id_right := fun _ _ f => feq_refl f;   (* f ∘ id ≡ f definitionally *)
  comp_assoc := fun _ _ _ _ f g h => feq_fapp_assoc h g f;
  comp_assoc_sym := fun _ _ _ _ f g h => feq_sym _ _ (feq_fapp_assoc h g f)
|}.

Notation "C □ D" := (@Funny C D) (at level 90) : category_scope.

