(** * The funny tensor product of categories

    nLab: https://ncatlab.org/nlab/show/funny+tensor+product

    The funny tensor product C □ D has the same objects as the cartesian
    product C ∏ D, but its morphisms (c1, d1) ~> (c2, d2) are words of steps,
    each step moving exactly ONE coordinate:

      an L-step  f : c1 ~> c2 in C   moves  (c1, d) ~> (c2, d);
      an R-step  g : d1 ~> d2 in D   moves  (c, d1) ~> (c, d2).

    The words are quotiented only by the relations that remember composition
    and identities in each coordinate separately.  Crucially there is NO
    relation commuting an L-step past an R-step: by contrast with C ∏ D, the
    square built from f : c1 ~> c2 and g : d1 ~> d2 does not commute in C □ D,
    and has two distinct diagonals (identified only by the comparison functor
    to C ∏ D).  The absence of this interchange law is the entire content of
    the construction.

    References:

    - F. Foltz, C. Lair, G. M. Kelly, "Algebraic categories with few monoidal
      biclosed structures or none", JPAA 17(2) (1980) 171-177,
      doi:10.1016/0022-4049(80)90082-1.  Up to isomorphism there are exactly
      two monoidal biclosed structures on Cat, and both are symmetric: the
      cartesian one (internal hom = functors and natural transformations) and
      the funny one (internal hom = functors and unnatural transformations).
    - Mark Weber, "Free products of higher operad algebras", TAC 28:2 (2013)
      24-65, §2: the generators-and-relations presentation used here, the
      pushout formula, and the comparison functor κ : C □ D ⟶ C ∏ D.
    - nLab, "funny tensor product": the pushout definition and the failure of
      equivalence-invariance.

    A load-bearing caveat: the funny tensor is NOT invariant under equivalence
    of categories (with I the "walking isomorphism" chaotic groupoid on two
    objects, 1 □ 1 ≅ 1 while I □ I is equivalent to the group ℤ, freely
    generated by the interchange loop).  Consequently − □ − does NOT define a
    bifunctor on this library's weak [Cat] (whose hom-setoid identifies
    functors up to natural isomorphism): its [fmap_respects] is false.  The
    bifunctor and the symmetric monoidal structure live on [StrictCat]
    instead — the same shape of problem as the issue #138 discussion in
    Instance/StrictCat.v.  See Construction/Funny/Closed.v for the
    corresponding no-go results about stating the closed structure via
    [Adjunction] or [ClosedMonoidal]. *)

Section FunnyHom.

Context {C : Category}.
Context {D : Category}.

(* A morphism (c1,d1) ~> (c2,d2) of C □ D: a word of steps, each moving ONE
   coordinate.  Four scalar indices, never a pair.  NO constructor and NO
   relation commutes an L-step past an R-step: absence of interchange is the
   entire content (Weber TAC 28:2 §2; nLab "funny tensor product"; FLK
   1980). *)
Inductive FunHom : C → D → C → D → Type :=
  | fnil {c : C} {d : D} : FunHom c d c d
  | fconsL {c1 c2 : C} {d1 : D} (f : c1 ~{C}~> c2)
           {c3 : C} {d3 : D} (t : FunHom c2 d1 c3 d3) : FunHom c1 d1 c3 d3
  | fconsR {c1 : C} {d1 d2 : D} (g : d1 ~{D}~> d2)
           {c3 : C} {d3 : D} (t : FunHom c1 d2 c3 d3) : FunHom c1 d1 c3 d3.

(* Singleton words: the generating steps. *)
Definition lstep {c1 c2 : C} {d : D} (f : c1 ~{C}~> c2) : FunHom c1 d c2 d :=
  fconsL f fnil.
Definition rstep {c : C} {d1 d2 : D} (g : d1 ~{D}~> d2) : FunHom c d1 c d2 :=
  fconsR g fnil.

(* Words read source-to-target; append composes indices on the nose. *)
Fixpoint fapp {c1 d1 c2 d2} (t : FunHom c1 d1 c2 d2) :
  ∀ {c3 d3}, FunHom c2 d2 c3 d3 → FunHom c1 d1 c3 d3 :=
  match t in FunHom a b c d
        return ∀ c3 d3, FunHom c d c3 d3 → FunHom a b c3 d3 with
  | fnil        => fun _ _ u => u
  | fconsL f t' => fun _ _ u => fconsL f (fapp t' u)
  | fconsR g t' => fun _ _ u => fconsR g (fapp t' u)
  end.

Lemma fapp_nil_r {c1 d1 c2 d2} (t : FunHom c1 d1 c2 d2) : fapp t fnil = t.
Proof.
  induction t; simpl.
  - reflexivity.
  - now rewrite IHt.
  - now rewrite IHt.
Qed.

Lemma fapp_assoc {c1 d1 c2 d2 c3 d3 c4 d4} (t : FunHom c1 d1 c2 d2)
      (u : FunHom c2 d2 c3 d3) (v : FunHom c3 d3 c4 d4) :
  fapp t (fapp u v) = fapp (fapp t u) v.
Proof.
  revert u v; induction t; simpl; intros.
  - reflexivity.
  - now rewrite IHt.
  - now rewrite IHt.
Qed.

(* Congruence: equivalence + cons-congruence (payload ≈-respect, mandatory
   for nested payloads) + head-position same-side fusion + head-position
   identity removal.  Type-valued: it is eliminated into the crelation ≈ of
   arbitrary target categories, so Prop is not an option.  Head+tail form
   makes append-congruence derivable ([fapp_feq_l]/[fapp_feq_r] below) with
   no app constructor.  Source-to-target order: the word "f then f'" fuses to
   the composite f' ∘ f. *)
Inductive feq : ∀ {c1 d1 c2 d2},
    FunHom c1 d1 c2 d2 → FunHom c1 d1 c2 d2 → Type :=
  | feq_refl {c1 d1 c2 d2} (t : FunHom c1 d1 c2 d2) : feq t t
  | feq_sym {c1 d1 c2 d2} (t u : FunHom c1 d1 c2 d2) : feq t u → feq u t
  | feq_trans {c1 d1 c2 d2} (t u v : FunHom c1 d1 c2 d2) :
      feq t u → feq u v → feq t v
  | feq_consL {c1 c2 : C} {d1 : D} (f f' : c1 ~{C}~> c2) {c3 d3}
      (t u : FunHom c2 d1 c3 d3) :
      f ≈ f' → feq t u → feq (fconsL f t) (fconsL f' u)
  | feq_consR {c1 : C} {d1 d2 : D} (g g' : d1 ~{D}~> d2) {c3 d3}
      (t u : FunHom c1 d2 c3 d3) :
      g ≈ g' → feq t u → feq (fconsR g t) (fconsR g' u)
  | feq_fuseL {c1 c2 c3 : C} {d1 : D} (f : c1 ~{C}~> c2) (f' : c2 ~{C}~> c3)
      {c4 d4} (t : FunHom c3 d1 c4 d4) :
      feq (fconsL f (fconsL f' t)) (fconsL (f' ∘ f) t)
  | feq_fuseR {c1 : C} {d1 d2 d3 : D} (g : d1 ~{D}~> d2) (g' : d2 ~{D}~> d3)
      {c4 d4} (t : FunHom c1 d3 c4 d4) :
      feq (fconsR g (fconsR g' t)) (fconsR (g' ∘ g) t)
  | feq_idL {c1 : C} {d1 : D} {c2 d2} (t : FunHom c1 d1 c2 d2) :
      feq (fconsL id t) t
  | feq_idR {c1 : C} {d1 : D} {c2 d2} (t : FunHom c1 d1 c2 d2) :
      feq (fconsR id t) t.

#[export] Program Instance FunHom_Setoid {c1 d1 c2 d2} :
  Setoid (FunHom c1 d1 c2 d2) := {| equiv := @feq c1 d1 c2 d2 |}.
Next Obligation.
  constructor.
  - intro t. apply feq_refl.
  - intros t u. apply feq_sym.
  - intros t u v. apply feq_trans.
Defined.

Lemma fapp_feq_l {c1 d1 c2 d2} {t t' : FunHom c1 d1 c2 d2} (E : feq t t') :
  ∀ {c3 d3} (u : FunHom c2 d2 c3 d3), feq (fapp t u) (fapp t' u).
Proof.
  induction E; simpl; intros x y w.
  - apply feq_refl.
  - apply feq_sym, IHE.
  - eapply feq_trans; [ apply IHE1 | apply IHE2 ].
  - apply feq_consL; [ assumption | apply IHE ].
  - apply feq_consR; [ assumption | apply IHE ].
  - apply feq_fuseL.
  - apply feq_fuseR.
  - apply feq_idL.
  - apply feq_idR.
Qed.

Lemma fapp_feq_r {c1 d1 c2 d2 c3 d3} (t : FunHom c1 d1 c2 d2)
      {u u' : FunHom c2 d2 c3 d3} (E : feq u u') : feq (fapp t u) (fapp t u').
Proof.
  revert u u' E; induction t; simpl; intros u u' E.
  - exact E.
  - apply feq_consL; [ reflexivity | exact (IHt _ _ E) ].
  - apply feq_consR; [ reflexivity | exact (IHt _ _ E) ].
Qed.

(* Binary [Proper] phrasing of append-congruence.  A plain lemma, NOT a
   registered instance: because [fapp]'s implicit target indices {c3 d3}
   intervene between its two word arguments, the eta-expanded subject below
   is not a partial application of [@fapp], so setoid rewriting — which keys
   on the head constant of a literal application — could never select it.
   [fapp_respects_forall] below is the operative rewrite instance. *)
Lemma fapp_respects {c1 d1 c2 d2 c3 d3} :
  Proper (equiv ==> equiv ==> equiv)
    (fun (t : FunHom c1 d1 c2 d2) (u : FunHom c2 d2 c3 d3) => fapp t u).
Proof.
  intros t t' Et u u' Eu.
  exact (feq_trans _ _ _ (fapp_feq_l Et u) (fapp_feq_r t' Eu)).
Qed.

(* The same fact keyed on [fapp]'s actual argument structure (the target
   indices intervene between the two word arguments), so that setoid
   [rewrite] works under literal [fapp] applications. *)
#[export] Instance fapp_respects_forall {c1 d1 c2 d2} :
  Proper (equiv ==>
            forall_relation (fun c3 =>
              forall_relation (fun d3 => equiv ==> equiv)))
    (@fapp c1 d1 c2 d2).
Proof.
  intros t t' Et c3 d3 u u' Eu.
  exact (feq_trans _ _ _ (fapp_feq_l Et u) (fapp_feq_r t' Eu)).
Qed.

(* [fapp] flipped into composition order: this is [compose] of the category
   C □ D below. *)
#[export] Instance funny_compose_respects {c1 d1 c2 d2 c3 d3} :
  Proper (equiv ==> equiv ==> equiv)
    (fun (t : FunHom c2 d2 c3 d3) (u : FunHom c1 d1 c2 d2) => fapp u t).
Proof.
  intros t t' Et u u' Eu.
  exact (feq_trans _ _ _ (fapp_feq_r u Et) (fapp_feq_l Eu t')).
Qed.

Lemma feq_fapp_fnil {c1 d1 c2 d2} (t : FunHom c1 d1 c2 d2) :
  feq (fapp t fnil) t.
Proof. rewrite fapp_nil_r. apply feq_refl. Qed.

Lemma feq_fapp_assoc {c1 d1 c2 d2 c3 d3 c4 d4} (t : FunHom c1 d1 c2 d2)
      (u : FunHom c2 d2 c3 d3) (v : FunHom c3 d3 c4 d4) :
  feq (fapp (fapp t u) v) (fapp t (fapp u v)).
Proof. rewrite fapp_assoc. apply feq_refl. Qed.

End FunnyHom.

