Lemma lstep_fuse {C D : Category} {c1 c2 c3 : C} {d : D}
  (f : c1 ~{C}~> c2) (f' : c2 ~{C}~> c3) :
  feq (fapp (lstep (d:=d) f) (lstep f')) (lstep (f' ∘ f)).
Proof. exact (feq_fuseL f f' fnil). Qed.

Lemma lstep_id {C D : Category} {c : C} {d : D} :
  feq (lstep (c1:=c) (d:=d) id) fnil.
Proof. exact (feq_idL fnil). Qed.

Lemma rstep_fuse {C D : Category} {c : C} {d1 d2 d3 : D}
  (g : d1 ~{D}~> d2) (g' : d2 ~{D}~> d3) :
  feq (fapp (rstep (c:=c) g) (rstep g')) (rstep (g' ∘ g)).
Proof. exact (feq_fuseR g g' fnil). Qed.

Lemma rstep_id {C D : Category} {c : C} {d : D} :
  feq (rstep (c:=c) (d1:=d) id) fnil.
Proof. exact (feq_idR fnil). Qed.

(* Every word decomposes at the head through a generating step; this equality
   is definitional (it is [t ∘[C □ D] lstep f] under the composition
   convention above), and is the joint-generation fact used by uniqueness
   arguments downstream. *)
Lemma fconsL_decomp {C D : Category} {c1 c2 : C} {d1 : D} {c3 : C} {d3 : D}
  (f : c1 ~{C}~> c2) (t : FunHom c2 d1 c3 d3) :
  fconsL f t = fapp (lstep f) t.
Proof. reflexivity. Qed.

Lemma fconsR_decomp {C D : Category} {c1 : C} {d1 d2 : D} {c3 : C} {d3 : D}
  (g : d1 ~{D}~> d2) (t : FunHom c1 d2 c3 d3) :
  fconsR g t = fapp (rstep g) t.
Proof. reflexivity. Qed.

(* ---- the separately-functorial eliminator (pushout universal property) ---- *)

Section FunnyUP.

Context {C : Category}.
Context {D : Category}.
Context {E : Category}.
Context {h : C → D → E}.

(* Separately functorial data: functorial in C at each d, functorial in D at
   each c, agreeing on objects via the SHARED object map h; NO mixed law and
   NO interchange condition relating the two actions (nLab: a functor out of
   C □ D is precisely a separately functorial assignment). *)
Record SepFunctorial : Type := {
  sf_lmap {c c' : C} (f : c ~{C}~> c') (d : D) : h c d ~{E}~> h c' d;
  sf_rmap (c : C) {d d' : D} (g : d ~{D}~> d') : h c d ~{E}~> h c d';
  sf_lmap_respects {c c' : C} {d : D} :
    Proper (equiv ==> equiv) (fun f : c ~{C}~> c' => sf_lmap f d);
  sf_rmap_respects {c : C} {d d' : D} :
    Proper (equiv ==> equiv) (fun g : d ~{D}~> d' => sf_rmap c g);
  sf_lmap_id {c : C} {d : D} : sf_lmap (@id C c) d ≈ id;
  sf_rmap_id {c : C} {d : D} : sf_rmap c (@id D d) ≈ id;
  sf_lmap_comp {c c' c'' : C} {d : D} (f' : c' ~{C}~> c'') (f : c ~{C}~> c') :
    sf_lmap (f' ∘ f) d ≈ sf_lmap f' d ∘ sf_lmap f d;
  sf_rmap_comp {c : C} {d d' d'' : D} (g' : d' ~{D}~> d'') (g : d ~{D}~> d') :
    sf_rmap c (g' ∘ g) ≈ sf_rmap c g' ∘ sf_rmap c g
}.

(* Fold a word through separately functorial data, one step at a time. *)
Fixpoint ffold (S : SepFunctorial) {c1 : C} {d1 : D} {c2 : C} {d2 : D}
    (t : FunHom c1 d1 c2 d2) : h c1 d1 ~{E}~> h c2 d2 :=
  match t in FunHom a b c d return h a b ~{E}~> h c d with
  | fnil        => id
  | fconsL f t' => ffold S t' ∘ sf_lmap S f _
  | fconsR g t' => ffold S t' ∘ sf_rmap S _ g
  end.

Lemma ffold_respects (S : SepFunctorial) {c1 : C} {d1 : D} {c2 : C} {d2 : D}
  {t u : FunHom c1 d1 c2 d2} (X : feq t u) : ffold S t ≈ ffold S u.
Proof.
  induction X; simpl.
  - reflexivity.
  - symmetry; exact IHX.
  - etransitivity; [ exact IHX1 | exact IHX2 ].
  - apply compose_respects; [ exact IHX | now apply (sf_lmap_respects S) ].
  - apply compose_respects; [ exact IHX | now apply (sf_rmap_respects S) ].
  - etransitivity;
      [ apply comp_assoc_sym
      | apply compose_respects;
          [ reflexivity | symmetry; apply (sf_lmap_comp S) ] ].
  - etransitivity;
      [ apply comp_assoc_sym
      | apply compose_respects;
          [ reflexivity | symmetry; apply (sf_rmap_comp S) ] ].
  - etransitivity;
      [ apply compose_respects; [ reflexivity | apply (sf_lmap_id S) ]
      | apply id_right ].
  - etransitivity;
      [ apply compose_respects; [ reflexivity | apply (sf_rmap_id S) ]
      | apply id_right ].
Qed.

Lemma ffold_app (S : SepFunctorial)
  {c1 : C} {d1 : D} {c2 : C} {d2 : D} {c3 : C} {d3 : D}
  (t : FunHom c1 d1 c2 d2) (u : FunHom c2 d2 c3 d3) :
  ffold S (fapp t u) ≈ ffold S u ∘ ffold S t.
Proof.
  revert u; induction t; simpl; intros u.
  - symmetry; apply id_right.
  - etransitivity;
      [ apply compose_respects; [ apply IHt | reflexivity ]
      | apply comp_assoc_sym ].
  - etransitivity;
      [ apply compose_respects; [ apply IHt | reflexivity ]
      | apply comp_assoc_sym ].
Qed.

Program Definition FunnyUP (S : SepFunctorial) : (C □ D) ⟶ E := {|
  fobj := fun x => h (fst x) (snd x);
  fmap := fun _ _ t => ffold S t
|}.
Next Obligation. proper. now apply ffold_respects. Qed.
Next Obligation. now apply ffold_app. Qed.

Lemma FunnyUP_lstep (S : SepFunctorial) {c c' : C} {d : D}
  (f : c ~{C}~> c') :
  @fmap (C □ D) E (FunnyUP S) (c, d) (c', d) (lstep f) ≈ sf_lmap S f d.
Proof. exact (id_left (sf_lmap S f d)). Qed.

Lemma FunnyUP_rstep (S : SepFunctorial) {c : C} {d d' : D}
  (g : d ~{D}~> d') :
  @fmap (C □ D) E (FunnyUP S) (c, d) (c, d') (rstep g) ≈ sf_rmap S c g.
Proof. exact (id_left (sf_rmap S c g)). Qed.

End FunnyUP.

Arguments SepFunctorial {C D E} h.

(* ---- transport-free uniqueness half of the universal property ---- *)

(* Every functor out of C □ D restricts to separately functorial data... *)
Program Definition toSep {C D E : Category} (H : (C □ D) ⟶ E) :
  SepFunctorial (fun c d => H (c, d)) := {|
  sf_lmap := fun c c' f d => @fmap (C □ D) E H (c, d) (c', d) (lstep f);
  sf_rmap := fun c d d' g => @fmap (C □ D) E H (c, d) (c, d') (rstep g)
|}.
Next Obligation.
  proper.
  apply fmap_respects.
  apply feq_consL; [ assumption | apply feq_refl ].
Qed.
Next Obligation.
  proper.
  apply fmap_respects.
  apply feq_consR; [ assumption | apply feq_refl ].
Qed.
Next Obligation.
  etransitivity.
  - apply fmap_respects. exact (feq_idL fnil).
  - exact (@fmap_id (C □ D) E H (c, d)).
Qed.
Next Obligation.
  etransitivity.
  - apply fmap_respects. exact (feq_idR fnil).
  - exact (@fmap_id (C □ D) E H (c, d)).
Qed.
Next Obligation.
  etransitivity.
  - apply fmap_respects. exact (feq_sym _ _ (feq_fuseL f f' fnil)).
  - exact (@fmap_comp (C □ D) E H (c, d) (c', d) (c'', d)
             (lstep f') (lstep f)).
Qed.
Next Obligation.
  etransitivity.
  - apply fmap_respects. exact (feq_sym _ _ (feq_fuseR g g' fnil)).
  - exact (@fmap_comp (C □ D) E H (c, d) (c, d') (c, d'')
             (rstep g') (rstep g)).
Qed.

(* ...and folding through that data recovers the functor: functors out of
   C □ D are determined by their action on the generating steps. *)
Lemma ffold_toSep {C D E : Category} (H : (C □ D) ⟶ E)
  {c1 : C} {d1 : D} {c2 : C} {d2 : D} (t : FunHom c1 d1 c2 d2) :
  ffold (toSep H) t ≈ @fmap (C □ D) E H (c1, d1) (c2, d2) t.
Proof.
  induction t as [ c d | ca cb da f cc dc t IHt | ca da db g cc dc t IHt ];
  simpl.
  - symmetry; exact (@fmap_id (C □ D) E H (c, d)).
  - etransitivity.
    + apply compose_respects; [ exact IHt | reflexivity ].
    + symmetry.
      exact (@fmap_comp (C □ D) E H (ca, da) (cb, da) (cc, dc) t (lstep f)).
  - etransitivity.
    + apply compose_respects; [ exact IHt | reflexivity ].
    + symmetry.
      exact (@fmap_comp (C □ D) E H (ca, da) (ca, db) (cc, dc) t (rstep g)).
Qed.

(* Two separately functorial data packages agreeing on the generating steps
   fold identically: uniqueness of the eliminator. *)
Lemma ffold_ext {C D E : Category} {h : C → D → E} (S S' : SepFunctorial h)
  (el : ∀ (c c' : C) (f : c ~{C}~> c') (d : D), sf_lmap S f d ≈ sf_lmap S' f d)
  (er : ∀ (c : C) (d d' : D) (g : d ~{D}~> d'), sf_rmap S c g ≈ sf_rmap S' c g)
  {c1 : C} {d1 : D} {c2 : C} {d2 : D} (t : FunHom c1 d1 c2 d2) :
  ffold S t ≈ ffold S' t.
Proof.
  induction t; simpl.
  - reflexivity.
  - apply compose_respects; [ exact IHt | apply el ].
  - apply compose_respects; [ exact IHt | apply er ].
Qed.

