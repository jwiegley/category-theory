(** The free category (path category) on the underlying quiver of C. *)

(* nLab: https://ncatlab.org/nlab/show/free+category
   Wikipedia: https://en.wikipedia.org/wiki/Free_category

   Wikipedia: "In mathematics, the free category or path category generated by
   a directed graph or quiver is the category that results from freely
   concatenating arrows together, whenever the target of one arrow is the
   source of the next."

   "More precisely, the objects of the category are the vertices of the
   quiver, and the morphisms are paths between objects. Here, a path is
   defined as a finite sequence " (of alternating vertices and edges).

   Here the "quiver" is the underlying directed graph of the ambient category
   C: its vertices are the objects of C and its edges the morphisms of C, all
   structure (composition, identities) forgotten. Concretely [Free] takes
   [obj := C] for vertices and [hom := tlist hom] for morphisms, where a
   [tlist hom x y] is a finite path of composable C-morphisms from x to y (see
   Lib/TList.v). The identity is the empty path [tnil] and composition is path
   concatenation [+++] (note [g +++ f] for [g ∘ f], since concatenation runs
   source-to-target while composition is written right-to-left); strict
   associativity is [tlist_app_assoc] and the unit laws are
   [tlist_app_tnil_l]/[tlist_app_tnil_r]. Morphism equivalence is plain
   equality [eq] of paths.

   This [Free] construction is the object map of the free-category functor
   that is left adjoint to the forgetful functor U : Cat ⟶ Quiv sending a
   category to its underlying quiver; [FreeFunctor : Free ⟶ C] below is the
   counit-style evaluation functor collapsing each path back to its composite
   in C, i.e. the unique functor extending the identity graph map on U C. The
   adjunction proper relating [Free] (paths) to [FreeSyntax] (formal syntax
   trees of composites) is established as [ForgetSyntax_CanonicalMap] at the
   end of this file. *)

(* Presentations, quivers, and normalization by evaluation

   nLab:  https://ncatlab.org/nlab/show/quiver
   Book:  Mac Lane, "Categories for the Working Mathematician", Springer
          GTM 5, 1971; 2nd ed. 1998, Chapter II
   Paper: Gabriel, "Unzerlegbare Darstellungen I", Manuscripta
          Mathematica 6, 1972
   Paper: van der Ploeg, Kiselyov, "Reflection without Remorse: Revealing
          a Hidden Sequence to Speed Up Monadic Reflection", Haskell
          Symposium 2014

   The textbook home of this construction is Mac Lane's Chapter II: §II.7
   "Graphs and Free Categories" builds the category of paths on a directed
   graph, and §II.8 "Quotient Categories" immediately quotients it, with
   Proposition 8.1 producing the category generated by a graph subject to
   relations among its paths — the categorical analogue of presenting a
   group or an algebra by generators and relations.  The free category is
   the relation-free case of that pair.  In this library the quotient half
   is Construction/Quotient.v, and Construction/PROP/Presentation.v runs
   the same recipe one level down for presented PROPs.  The general
   adjunction over arbitrary setoid-edged quivers is developed in
   Construction/Free/Quiver.v ([FreeForgetfulAdjunction]); it lives over
   StrictCat rather than the weak Cat because a category's underlying
   quiver is not invariant under natural isomorphism, a point locked in by
   Test/Issue138.v.  The adjunction is moreover monadic: categories are
   exactly the algebras for the paths monad on graphs (Leinster, "Higher
   Operads, Higher Categories", CUP 2004, Theorem 6.5.2), so composition
   is structure a graph may carry rather than data of a different shape.

   The word "quiver" is Gabriel's coinage — "we propose the term quiver,
   and not graph, since there are already too many notions attached to the
   latter word" (Gabriel 1972, quoted at the nLab page above) — and his
   setting is this file read in reverse: forget the composition law of a
   category, then study linear representations of what remains — functors
   from the free category to Vect — with the quivers of finite
   representation type classified by the ADE Dynkin diagrams.  Applied
   category theory runs the same recipe toward data: a database schema is
   a finitely presented category, a free category on tables and foreign
   keys quotiented by path equations, with instances as functors to Set
   and data migration by adjoints (Fong, Spivak, "Seven Sketches in
   Compositionality", CUP 2019, Chapter 3).  The one-object case is the
   free monoid, as the header's Wikipedia entry records: on a
   single-vertex quiver the paths are words over the edge set under
   concatenation, the reading behind lists in Theory/Coq/List.v and the
   monoid of objects of a PROP (Construction/PROP.v).

   The morphism carrier [tlist hom] is the type-aligned sequence of van
   der Ploeg and Kiselyov: a sequence whose element types line up endpoint
   to endpoint, which is to say a morphism of a free category.  They
   deploy such sequences to repair the quadratic cost of left-associated
   binds in free monads, iteratees, and extensible effects, and
   Lib/TList.v cites van der Ploeg's type-aligned library directly.

   The second half of this file is normalization by evaluation, stated
   categorically.  [Mor] is the raw syntax of composites, a [tlist] path
   is its normal form, [morDA] is the normalizer — flattening erases
   exactly the associativity and unit bureaucracy — and [Mor_Setoid]
   compares terms by normal form, while [morD] gives their semantics in
   C.  Concatenation is strictly associative and unital, which is why
   [Free] can carry bare [eq] as its morphism equivalence; it follows
   that two formal composites are identified by the category laws
   precisely when their edge paths coincide, the semantic core of
   reflective tactics that decide category-law equations.  The asymmetry
   of the round trips noted at [tlistDA_morDA] is the general signature
   of a normalizer: normal forms are stable under reflect-then-normalize,
   whereas arbitrary syntax is recovered only up to the laws. *)

Program Definition Free@{fo fh fp} : Category@{fo fh fp} := {|
  obj     := C;                              (* vertices = objects of C *)
  hom     := tlist hom;                      (* morphisms = paths of C-arrows *)
  homset  := fun _ _ => {| equiv := eq |};   (* paths compared by equality *)
  id      := fun _ => tnil;                  (* identity = empty path *)
  compose := fun _ _ _ f g => g +++ f        (* composition = path concatenation *)
|}.
Next Obligation. equivalence; congruence. Qed.
Next Obligation. now apply tlist_app_tnil_r. Qed.
Next Obligation. now apply tlist_app_assoc. Qed.
Next Obligation. now symmetry; apply tlist_app_assoc. Qed.

(* Evaluate a path back to its composite C-morphism. This is the action on
   morphisms of the evaluation functor [FreeFunctor] below; [composition_tnil]
   and [composition_tapp] show it preserves identities and composition (the
   latter being functoriality, fmap distributing over [+++]). *)
Definition composition {x y : C} : tlist hom x y → x ~{C}~> y.
Proof.
  intros.
  induction X.
  - exact id.
  - exact (compose IHX b).
Defined.

Definition composition_tnil {x : C} : composition tnil ≈ id[x].
Proof. now cat. Qed.

Definition composition_tapp {x y z : C}
  (g : tlist hom y z) (f : tlist hom x y) :
  composition (f +++ g) ≈ composition g ∘ composition f.
Proof.
  induction f; simpl.
  - rewrite tlist_app_tnil_l.
    now cat.
  - rewrite <- tlist_app_comm_cons.
    simpl.
    rewrite IHf.
    now cat.
Qed.

#[local] Obligation Tactic := cat_simpl; try reflexivity.

(* The evaluation functor Free ⟶ C: the identity on objects, sending each path
   to the composite of its edges. This is the counit ε : Free (U C) ⟶ C of the
   free-forgetful adjunction, i.e. the unique functor extending the identity
   graph map on the underlying quiver U C. *)
Program Definition FreeFunctor : Free ⟶ C := {|
  fobj := fun x => x;
  fmap := fun _ _ f => composition f;
  fmap_id := fun _ => composition_tnil;
  fmap_comp := fun _ _ _ => composition_tapp
|}.

(**************************************************************************)

(* An alternative presentation of the free category as formal syntax trees of
   composites, rather than flattened paths. A [Mor x y] is an unnormalized term
   built from identities, generating edges, and binary composition; quotienting
   by the category laws (via [Mor_Setoid] below, which compares terms by their
   flattened path [morDA]) recovers the same free category as [Free]. *)
Inductive Mor : C → C → Type :=
  | Ident {x} : Mor x x                                  (* identity term *)
  | Morph {x y} (f : x ~> y) : Mor x y                   (* generating edge *)
  | Comp  {x y z} (f : Mor y z) (g : Mor x y) : Mor x z. (* composite term *)

(* Evaluate a syntax term to its composite C-morphism (the Mor analogue of
   [composition], factoring through C directly). *)
Fixpoint morD `(t : Mor x y) : x ~> y :=
  match t with
  | Ident => id
  | Morph f => f
  | Comp f g => morD f ∘ morD g
  end.

(* Flatten a syntax term to its path; this is the normal form that
   [Mor_Setoid] compares terms by, and the action on morphisms of the
   forgetful map [ForgetSyntax : FreeSyntax ⟶ Free] below. *)
Fixpoint morDA `(t : Mor x y) : tlist hom x y :=
  match t with
  | Ident => tnil
  | Morph f => tcons _ f tnil
  | Comp f g => morDA g +++ morDA f
  end.

(* Two syntax terms are equivalent exactly when they flatten to the same path,
   i.e. when they are equal modulo the category laws (associativity and the
   unit laws built into [tlist] concatenation). *)
Program Instance Mor_Setoid {x y} : Setoid (Mor x y) := {
  equiv f g := morDA f = morDA g
}.

Lemma morD_sound `{t : Mor x y} :
  morD t ≈ composition (morDA t).
Proof.
  induction t; simpl; cat.
  rewrite IHt1, IHt2; simpl.
  now rewrite composition_tapp.
Qed.

(* Reflect a path back into a right-nested syntax term; the section/retraction
   pair [morDA_tlistDA] (a strict equality) and [tlistDA_morDA] (only an
   equivalence, since syntax terms are not normalized) witness the two
   directions. *)
Fixpoint tlistDA `(t : tlist hom x y) : Mor x y :=
  match t with
  | tnil => Ident
  | tcons _ f fs => Comp (tlistDA fs) (Morph f)
  end.

(* Note that this yields an equality. *)
Lemma morDA_tlistDA `{f : tlist hom x y} :
  morDA (tlistDA f) = f.
Proof.
  induction f; simpl; auto.
  rewrite <- tlist_app_cons.
  now rewrite IHf.
Qed.

(* While this is merely an equivalence, not an equality: a syntax term and the
   reflection of its flattened path agree only up to the category laws, since
   [Mor] terms are not normalized. This asymmetry (strict in one direction,
   up-to-≈ in the other) is exactly what makes the [Mor]/[tlist] correspondence
   an adjoint equivalence rather than an isomorphism of the two presentations. *)
Lemma tlistDA_morDA `{f : Mor x y} :
  tlistDA (morDA f) ≈ f.
Proof.
  induction f; simpl; auto.
  rewrite <- IHf1, <- IHf2.
  now rewrite morDA_tlistDA.
Qed.

(* The same free category presented by syntax terms [Mor] up to ≈, with the
   identity given by [Ident] and composition by the [Comp] constructor (note no
   normalization happens here; the laws hold only because morphisms are
   compared via their flattened paths). *)
Program Definition FreeSyntax : Category := {|
  obj     := C;                     (* vertices = objects of C *)
  hom     := Mor;                   (* morphisms = syntax terms up to ≈ *)
  homset  := @Mor_Setoid;           (* terms compared by flattened path *)
  id      := fun _ => Ident;        (* identity = Ident term *)
  compose := fun _ _ _ => Comp      (* composition = Comp constructor *)
|}.
Next Obligation. now apply tlist_app_tnil_r. Qed.
Next Obligation. now apply tlist_app_assoc. Qed.
Next Obligation. now symmetry; apply tlist_app_assoc. Qed.

(**************************************************************************)

(* Normalization functor: identity on objects, flattening each syntax term to
   its path. Faithful and full by [morDA_tlistDA]/[tlistDA_morDA]. *)
Program Instance ForgetSyntax : FreeSyntax ⟶ Free := {
  fobj := λ x, x;
  fmap := λ x y f, morDA f;
}.

(* Reflection functor: identity on objects, embedding each path as a
   right-nested syntax term. Its right adjoint, [ForgetSyntax ⊣ CanonicalMap]
   below. *)
Program Instance CanonicalMap : Free ⟶ FreeSyntax := {
  fobj := λ x, x;
  fmap := λ x y f, tlistDA f;
}.
Next Obligation.
  generalize dependent f.
  generalize dependent z.
  induction g; simpl; intros.
  - now rewrite !tlist_app_tnil_l.
  - rewrite <- tlist_app_cons.
    rewrite <- !tlist_app_comm_cons.
    simpl.
    rewrite <- tlist_app_cons.
    now rewrite <- IHg.
Qed.

(**************************************************************************)

(* The adjunction ForgetSyntax ⊣ CanonicalMap relating the two presentations of
   the free category. The hom-set isomorphism transposes via the conversion
   maps [tlistDA] (path → term) and [morDA] (term → path); because [morDA] is a
   strict retraction of [tlistDA] ([morDA_tlistDA]) while the reverse holds only
   up to ≈ ([tlistDA_morDA]), this is in fact an adjoint equivalence: [Free] and
   [FreeSyntax] are the same free category, one in normalized (path) form and
   one in raw syntactic form. *)
Program Instance ForgetSyntax_CanonicalMap :
  ForgetSyntax ⊣ CanonicalMap := {
  adj := λ x y,
    {| to   := {| morphism := tlistDA |}
     ; from := {| morphism := morDA |} |}
}.
Next Obligation.
  simpl; intros;
  now rewrite tlistDA_morDA.
Qed.
Next Obligation.
  simpl; intros;
  now rewrite morDA_tlistDA.
Qed.
Next Obligation.
  now rewrite !morDA_tlistDA.
Qed.
Next Obligation.
  now rewrite !morDA_tlistDA.
Qed.
Next Obligation.
  now rewrite !morDA_tlistDA.
Qed.

End Free.
